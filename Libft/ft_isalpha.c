#include "libft.h"

int	ft_isalpha(int c)
{
	unsigned char	cc;

	cc = (unsigned char)c;
	if ((cc >= 'A' && cc <= 'Z') || (cc >= 'a' && cc <= 'z'))
		return (1);
	else
		return (0);
}
